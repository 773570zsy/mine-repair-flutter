def main():
    db = sqlite3.connect(DB_PATH)
    cur = db.cursor()

    existing_count = cur.execute('SELECT COUNT(*) FROM quiz_questions').fetchone()[0]
    print(f'现有题库: {existing_count} 题')

    # 清空重导
    cur.execute('DELETE FROM quiz_questions')
    print('已清空旧数据')

    # 用 question 文本做去重
    seen = set()
    added = 0
    skipped = 0

    for q in ALL_QUESTIONS:
        q_text = q[0].strip()
        if q_text in seen:
            skipped += 1
            continue
        seen.add(q_text)

        # 打散选项，均衡答案分布
        new_opts, new_ans = rebalance_answer(q[2], q[3])

        cur.execute(
            'INSERT INTO quiz_questions (question, type, options, answer, explanation, category) VALUES (?, ?, ?, ?, ?, ?)',
            [q[0], q[1], new_opts, new_ans, q[4], q[5]]
        )
        added += 1

    db.commit()

    # 统计
    total = cur.execute('SELECT COUNT(*) FROM quiz_questions').fetchone()[0]
    print(f'\n--- 导入结果 ---')
    print(f'新增: {added} 题（跳过重复: {skipped} 题）')
    print(f'题库总数: {total} 题')

    print('\n按类别统计:')
    for cat, cnt in cur.execute('SELECT category, COUNT(*) as c FROM quiz_questions GROUP BY category ORDER BY c DESC').fetchall():
        print(f'  {cat}: {cnt}题')

    print('\n答案分布:')
    for ans, cnt in cur.execute('SELECT answer, COUNT(*) as c FROM quiz_questions GROUP BY answer ORDER BY c DESC').fetchall():
        pct = cnt / total * 100
        print(f'  {ans}: {cnt}题 ({pct:.1f}%)')

    db.close()
    print('\n题库导入完成!')

if __name__ == '__main__':
    main()
