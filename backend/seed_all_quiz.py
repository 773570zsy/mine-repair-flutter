def rebalance_answer(options_json, correct_letter):
    """随机打散选项顺序，返回 (new_options_json, new_answer_letter)"""
    opts = json.loads(options_json)
    if len(opts) != 4:
        return options_json, correct_letter

    correct_idx = ord(correct_letter.upper()) - ord('A')
    if correct_idx < 0 or correct_idx >= len(opts):
        return options_json, correct_letter

    correct_text = opts[correct_idx]

    # 生成随机排列索引
    indices = list(range(4))
    random.shuffle(indices)
    new_opts = [opts[i] for i in indices]
    new_correct_idx = indices.index(correct_idx)
    new_letter = chr(ord('A') + new_correct_idx)

    return json.dumps(new_opts, ensure_ascii=False), new_letter

