async function main() {
  await initDB();
  const db = getDB();
  const before = db.prepare('SELECT COUNT(*) as c FROM quiz_questions').get().c;
  let added = 0, skipped = 0;

  for (const x of ALL) {
    const exist = db.prepare('SELECT id FROM quiz_questions WHERE question = ?').get(x.q);
    if (exist) { skipped++; continue; }
    db.prepare(
      'INSERT INTO quiz_questions (question, type, options, answer, explanation, category) VALUES (?,?,?,?,?,?)'
    ).run(x.q, 'choice', JSON.stringify(x.o), x.a, x.e, x.c);
    added++;
  }

  saveDB(); // 关键：持久化到磁盘
  const after = db.prepare('SELECT COUNT(*) as c FROM quiz_questions').get().c;
  console.log(`新增 ${added} | 跳过 ${skipped} | 总计 ${after} 题`);
}

main().catch(e => console.error(e));
