/// 获取中文文本的拼音首字母
/// 如：发动机皮带 → FDJPD
/// 非中文/未收录字符保留原字符
String toPinyinInitials(String text) {
  if (text.isEmpty) return '';
  final buf = StringBuffer();
  for (int i = 0; i < text.length; i++) {
    final ch = text[i];
    final py = _pinyinMap[ch];
    if (py != null) {
      buf.write(py);
    } else {
      buf.write(ch.toUpperCase());
    }
  }
  return buf.toString();
}

/// 检查文本是否匹配搜索词（支持中文、拼音首字母、英文模糊匹配）
/// query 可以是：中文 "发动"、拼音首字母 "FDJ"、英文 "cable"
bool matchPinyin(String text, String query) {
  if (query.isEmpty) return true;
  final lowerQuery = query.toLowerCase();
  final lowerText = text.toLowerCase();

  // 1. 直接包含（中文/英文）
  if (lowerText.contains(lowerQuery)) return true;

  // 2. 拼音首字母匹配（query 全大写才做拼音匹配）
  if (query == query.toUpperCase() && query.length > 0) {
    final py = toPinyinInitials(text).toLowerCase();
    if (py.contains(lowerQuery)) return true;
  }

  // 3. 每个字的拼音首字母前缀匹配
  // 如 query="ls" 匹配 "螺丝(lsh)"
  final initials = toPinyinInitials(text).toLowerCase();
  if (initials.startsWith(lowerQuery)) return true;

  return false;
}
